package variablesdatos;

public class DemoVariablesTiposDatos {

	public static void main(String[] args) {
		// este es un comentario de una sola linea.
				/*comentarios de multiples lineas, debajo hay varias
				 * ubicaciones en memoria con nombre de diferentes tipos
				 * de datos primitivos.
				 * */
				
				int nota; //Declaracion de una varibale tipo de dato primitivos.
				
				nota = 10; //10 es asignado a la ubicacion en memoria llamada nota,
				           //la cual es tipo de dato primitivo int. A esto
				           //tambien se le llama inicializacion
				//Ahora a imprimir algo en pantalla
				System.out.println("nota tiene el valor de: " + nota);
				
				double salario = 1250.99; //Declaracion e inicializacion.
				System.out.println("salario tiene el valor de: " + salario);

	}

}
