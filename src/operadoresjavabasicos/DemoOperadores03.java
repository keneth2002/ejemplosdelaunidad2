package operadoresjavabasicos;

public class DemoOperadores03 {

	public static void main(String[] args) {
		//operadores unarios
		int x = 10;
		System.out.println("x = " + x);
		--x; //Predecremento
		System.out.println("x = " + x);
		//Predecremento, el valor decrementado
		//Se muestra en la misma sentencia
		System.out.println("x = " + --x);
		System.out.println("x = " + x);

	}

}
