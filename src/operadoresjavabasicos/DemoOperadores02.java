package operadoresjavabasicos;

public class DemoOperadores02 {

	public static void main(String[] args) {
		//operadores unarios
		int x = 10;
		System.out.println("x = " + x);
		x++; //postincremento
		System.out.println("x = " + x);
		//postincremento, pero el valor incrementado
		System.out.println("x = " + x++);
		//se muestra en la siguiente sentencia 
		System.out.println("x = " + x);

	}

}
